    private void btnSaveT1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveT1ActionPerformed
        // TODO add your handling code here:
        for(int i=0;i<teacherprofilearray.getTeacherProfileArray().size();i++){
            if(teacherprofilearray.getTeacherProfileArray().get(i).getUsername().equals(teacherusername)){
                teacherprofilearray.getTeacherProfileArray().get(i).setFullName(txtTeacherFullName.getText());
                teacherprofilearray.getTeacherProfileArray().get(i).setEmailId(txtTeacherEmailId.getText());
                teacherprofilearray.getTeacherProfileArray().get(i).setMobNo(txtTeacherMobile.getText());
                teacherprofilearray.getTeacherProfileArray().get(i).setAge(txtTeacherAge.getText());
                teacherprofilearray.getTeacherProfileArray().get(i).setUsername(txtTeacherUsername.getText());
                teacherprofilearray.getTeacherProfileArray().get(i).setPassword(txtTeacherPassword.getText());
                JOptionPane.showMessageDialog(this, "Profile Updated Successfully !!");
            }
        }
    }//GEN-LAST:event_btnSaveT1ActionPerformed

