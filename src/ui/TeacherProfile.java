    private void display() {
        
        for(int i=0;i<teacherprofilearray.getTeacherProfileArray().size();i++){
            if(teacherprofilearray.getTeacherProfileArray().get(i).getUsername().equals(teacherusername)){
                txtTeacherFullName.setText(teacherprofilearray.getTeacherProfileArray().get(i).getFullName());
                txtTeacherEmailId.setText(teacherprofilearray.getTeacherProfileArray().get(i).getEmailId());
                txtTeacherMobile.setText(teacherprofilearray.getTeacherProfileArray().get(i).getMobNo());
                txtTeacherAge.setText(teacherprofilearray.getTeacherProfileArray().get(i).getAge());
                txtTeacherUsername.setText(teacherprofilearray.getTeacherProfileArray().get(i).getUsername());
                txtTeacherPassword.setText(teacherprofilearray.getTeacherProfileArray().get(i).getPassword());
                
                if(teacherprofilearray.getTeacherProfileArray().get(i).getGender().equals("Male")){
                    btnTeacherMale.setSelected(true);
                    btnTeacherFemale.setSelected(false);
                    btnTeacherFemale.setEnabled(false);
                    btnTeacherMale.setEnabled(false);
                }
                else if(teacherprofilearray.getTeacherProfileArray().get(i).getGender().equals("Female")){
                    btnTeacherFemale.setSelected(true);
                    btnTeacherMale.setSelected(false);
                    btnTeacherFemale.setEnabled(false);
                    btnTeacherMale.setEnabled(false);
                }
            }
        }
    }
